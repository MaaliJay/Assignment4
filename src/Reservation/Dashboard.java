    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        String name = txt_feedname.getText();
        String feedback = txt_feedback.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/user_account?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "mysqluserpwd");
            String sqlquery = "INSERT INTO feedback (name,feedback)VALUES ('" + name + "','" + feedback + "')";
            PreparedStatement pst = conn.prepareStatement(sqlquery);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Thank you for your feedback...!");
            txt_feedback.setText("");
            txt_feedname.setText("");
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_submitActionPerformed

